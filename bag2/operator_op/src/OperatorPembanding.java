public class OperatorPembanding {

    public static void main(String[] args){
        int nilaiA = 12;
        int nilaiB = 4;
        boolean hasil;

        //apakah a lebih besar dari b
        hasil = nilaiA > nilaiB;
        System.out.println(hasil);

        //apakah a lebih kecil dari b
        hasil = nilaiA < nilaiB;
        System.out.println(hasil);

        //apakah a lebih besar sama dengan dari b
        hasil = nilaiA >= nilaiB;
        System.out.println(hasil);

        //apakah a lebih kecil samadengan dari b
        hasil = nilaiA <= nilaiB;
        System.out.println(hasil);

        //apakah a sama degan dari b
        hasil = nilaiA == nilaiB;
        System.out.println(hasil);

        //apakah a tidak sama dngan dari b
        hasil = nilaiA != nilaiB;
        System.out.println(hasil);
    }
}
