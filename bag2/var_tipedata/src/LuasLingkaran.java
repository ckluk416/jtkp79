public class LuasLingkaran {
    public static void main(String[] args){
        //deklarasi
        double luas, pi;
        int r;

        //input
        pi = 3.14;
        r = 18;

        //proses
        luas = pi * r * r;

        //output
        System.out.println("Luas lingkaran = " + luas);
    }
}
